
public interface AdvancedOperations {

	public String sqMethod(double x); //square root of x

	public String absMethod(int x); //absolute value of x

	public String evenMethod(int x); //is x an even number or not?

	public String maxMethod(int x, int y); //which is largest of x and y?

	public String minMethod(int x, int y); //which is smallest of x and y?
}
